% --- Executes on button press in solvebutton.
function solvebutton_Callback(hObject, eventdata, handles)
% hObject    handle to solvebutton (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

global solutions;
solutions = [];

%% Checks of the board is entered correctly
ifWrong = 0;
for i = 1:81
    
    value = eval(['handles.edit', num2str(i),'.String']);
    
    
    if ~(length(value) == 1 && isstrprop(value, 'digit') &&...
            str2num(value) >= 1 && str2num(value) <= 9)
        if ~strcmp(value, '')
            handles.(['edit', num2str(i)]).String = '';
            ifWrong = 1;
        end
    end
    
end

if ifWrong
   return; 
end



%% Loads the Board
z = zeros(9,9);

for i = 1:81
    y = num2str(i);
    v = eval(['handles.edit',y,'.String']);
    
    if isempty(v)
        value = 0;
    else
        value = str2double(v);
    end
    if rem(i,9) == 0
        col = 9;
        row = floor(i/9);
    else
        col = rem(i,9);
        row = floor(i/9) + 1;
    end
    z(row, col) = value;
end

%% Solves the Board
global timer;
timer = cputime;
solve(z);
sol_board = solutions;

disp(sol_board);

disp(size(sol_board, 2));

if size(sol_board,2) > 9
    handles.messages.String = 'This puzzle is flawed!';
elseif size(sol_board,2) == 9
    for i = 1:81
        y = num2str(i);
        if rem(i,9) == 0
            col = 9;
            row = floor(i/9);
        else
            col = rem(i,9);
            row = floor(i/9) + 1;
        end
        handles.(['sol', y]).String = num2str(sol_board(row,col));
    end
    outputString = ["Time: ", num2str(cputime - timer)];
    handles.messages.String = strjoin(outputString);
else
    handles.messages.String = 'No solution';
end


